enum Direction {
	LEFT,
	RIGHT,
	UP,
	DOWN,
}

function getDirectionValue(d: Direction): number {
	if(d == Direction.LEFT) {
		return 12;
	}
	else if(d == Direction.RIGHT) {
		return 4;
	}
	else if(d == Direction.UP) {
		return 0;
	}
	else {
		return 8;
	}
}

